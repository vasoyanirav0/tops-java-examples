
/*
 * Final Keyword :  It is declared with final keyword.
 * - You can declare variable, method & class as a final.
 * - If you declare method as a final then you can't override it.
 * - If you declare variable as a final then you cannot change it's value.
 * - If you declare class as a final then you cannot extend it.
 * */

class final1
{
	final int a;
	public final1() {
		a=10;
	}
	void show()
	{
		
	}
}
class final2 extends final1
{
	void show()
	{
		
	}
}
